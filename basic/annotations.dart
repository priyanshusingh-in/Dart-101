// Explicit type annotations in Dart

void main() {
  String name = 'Mario';

  print(name);

  int age = 23;
  print(age);

  bool isOpen = false;
  print(isOpen);

  double averageRating = 4.3;
  print(averageRating);

  var fruit = ['apple', 'banana', 'orange'];
  print(fruit);

  //null safety in Dart
  int? point;
  print(point);
}
